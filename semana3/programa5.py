numero1 = input("numero1: ")#Se asigna input para que el usuario le de un valor a la variable
numero2 = input("numero2: ")#Se asigna input para que el usuario le de un valor a la variable
suma = int(numero1) + int(numero2)#Se pone la operacion que se realizara 
multiplicacion = int(numero1) * int(numero2)#Se pone la operacion que se realizara
resta = int(numero1) - int(numero2)#Se pone la operacion que se realizara
division = int(numero1) / int(numero2)#Se pone la operacion que se realizara
potencia = int(numero1) ** int(numero2)#Se pone la operacion que se realizara
print ("Suma= ",suma)#Se manda a imprimir el resultado de las operaciones anteriores
print ("Resta= ",resta)#Se manda a imprimir el resultado de las operaciones anteriores
print ("Multiplicación= ",multiplicacion)#Se manda a imprimir el resultado de las operaciones anteriores
print ("Potencia= ",potencia)#Se manda a imprimir el resultado de las operaciones anteriores
