numero1 = 10
numero2 = 5
print ("{n1} + {n2} = {suma}".format(n1=numero2,n2=numero2,suma = numero2 + numero2))
print ("{n1} {n2}".format(n1=numero1,n2=numero1))
print ("{n1} / {n2} = {split}".format(n1=numero1,n2=numero2,split = numero2 / numero1))
print ("{n1} * {n2} = {matriz}".format(n1=numero1,n2=numero2,matriz = numero2 * numero1))
print ("{n1} - {n2} = {resta}".format(n1=numero2,n2=numero1,resta = numero2 - numero1))